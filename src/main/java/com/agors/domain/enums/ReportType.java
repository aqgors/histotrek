package com.agors.domain.enums;

/**
 * Перелік типів звітів, що можуть бути сформовані в системі Histotrek.
 * <p>
 * Використовується для класифікації звітів у базі даних та інтерфейсі користувача.
 * </p>
 *
 * <ul>
 *   <li>{@code BEST_RATED} — Звіт про найвищі рейтинги місць</li>
 *   <li>{@code POPULAR_PLACES} — Найпопулярніші місця за кількістю відгуків або переглядів</li>
 *   <li>{@code USER_ACTIVITY} — Активність користувачів (кількість відгуків, оцінок тощо)</li>
 *   <li>{@code SYSTEM_LOG} — Системні дії (логіни, помилки, зміни даних)</li>
 *   <li>{@code TEXT_EXPORT} — Звіт, згенерований у форматі тексту для експорту (TXT/DOCX)</li>
 * </ul>
 *
 * @author agors
 * @version 1.0
 */
public enum ReportType {

    /** Звіт про найкраще оцінені місця. */
    BEST_RATED,

    /** Звіт про найпопулярніші історичні місця. */
    POPULAR_PLACES,

    /** Звіт про активність користувачів у системі. */
    USER_ACTIVITY,

    /** Журнал дій або лог системи. */
    SYSTEM_LOG,

    /** Експортований звіт у текстовому форматі. */
    TEXT_EXPORT
}
